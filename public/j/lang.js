function getlang(id, data) {
	var translation = 'lang error';
	if (lang_strings[get_language()] && typeof (lang_strings[get_language()][id]) == 'string') {
		translation = lang_strings[get_language()][id];
	} else if (lang_strings[defaults.language] && typeof (lang_strings[defaults.language][id]) == 'string') {
		translation = lang_strings[defaults.language][id];
	}

	if (data && translation.indexOf('{{data}}') !== -1) {
		translation = translation.replace('{{data}}', data);
	}

	return translation;

}

function lang_append(data){
	for(var key in data){
		lang_strings[key] = $.extend((lang_strings[key]||{}), data[key]);
	}

	return lang_strings;
}

